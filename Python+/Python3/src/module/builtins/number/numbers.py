# Number data types

# sum
a = 1 + 2
assert a == 3

# floor division
b = 10 // 3
assert b == 3

# remainder of the division
c = 10 % 3
assert c == 1

# power
d = 5 ** 2
assert d == 25

# floating point operations
e = 5.2 * .3
assert e == 1.56

# number of digits in int
f = 12345
n = len(str(f))
assert n == 5
